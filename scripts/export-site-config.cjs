/**
 * Write site/contracts.js from deployed contract addresses.
 *
 * Usage after deploy:
 *   DEPLOYED_ADDRESSES='{"SentinelToken":"0x...","AetheronBridge":"0x..."}' \
 *   EXPLORER_BASE_URL=https://sepolia.etherscan.io/address \
 *   node scripts/export-site-config.js
 *
 * Writes site/contracts.js which is loaded by site/index.html before script.js.
 * The file exports window.SENTINEL_CONTRACTS so script.js can read live addresses.
 */

const fs = require('fs');
const path = require('path');

const OUTPUT_PATH = path.join(__dirname, '..', 'site', 'contracts.js');

function main() {
  const rawAddresses = process.env.DEPLOYED_ADDRESSES || '{}';
  const addresses = JSON.parse(rawAddresses);
  const explorerBase =
    process.env.EXPLORER_BASE_URL || 'https://sepolia.etherscan.io/address';
  const network =
    process.env.HARDHAT_NETWORK || process.env.NETWORK || 'sepolia';

  const contracts = {};
  for (const [name, address] of Object.entries(addresses)) {
    contracts[name] = {
      address,
      explorerUrl: `${explorerBase}/${address}`,
    };
  }

  const output = `// Auto-generated by scripts/export-site-config.js
// Do not edit manually — regenerate with: npm run export:site-config
window.SENTINEL_CONTRACTS = ${JSON.stringify(contracts, null, 2)};
window.SENTINEL_NETWORK = ${JSON.stringify(network)};
`;

  fs.mkdirSync(path.dirname(OUTPUT_PATH), { recursive: true });
  fs.writeFileSync(OUTPUT_PATH, output, 'utf8');
  console.log(
    `Written site/contracts.js with ${
      Object.keys(contracts).length
    } contracts.`,
  );
}

main();
